    public boolean checkValidation(){
        if(btn_PHMoi.isSelected()){
            if(lbl_ValidCCCD.getText()==null&&lbl_ValidHoTen.getText()==null&&lbl_ValidNamSinh.getText()==null&&
                    lbl_ValidDanToc.getText()==null&&lbl_ValidDiaChi.getText()==null&&lbl_ValidSDT.getText()==null&&
                    lbl_ValidEmail.getText()==null){
                return true;
            } else{
                return false;
            }
        } else {
            if(btn_PHCu.isSelected()){
                if(lbl_ValidMaPH.getText()==null){
                    return true;
                } else{
                    return false;
                }
            }
        
        }
        return false;
    }
