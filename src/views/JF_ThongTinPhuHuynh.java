    public void HienThiThongTinPH(BienToanCuc btc){
        NguoiDung ph = btc.phuhuynh;
        
        if(btc.luaChonPH == 1){
            btn_PHMoi.setSelected(true);
            txt_MaPH.setText(ph.getMaND());
            txt_TenDN.setText(ph.getTenDN());
            txt_HoTen.setText(ph.getHoTen());
            txt_NamSinh.setText(ph.getNamSinh()+"");

            if(ph.isGioiTinh()){
                btn_Nam.setSelected(true);
            } else {
                btn_Nu.setSelected(true);
            }

            txt_DanToc.setText(ph.getDanToc());
            txt_CCCD.setText(ph.getCccd());
            txt_DiaChi.setText(ph.getDiaChi());
            txt_SDT.setText(ph.getSoDT());
            txt_Email.setText(ph.getEmail());
            
            //Label validation --> cho bằng null khi ban đầu chọn 1 học sinh --> có sẵn thông tin PH
            lbl_ValidCCCD.setText(null);
            lbl_ValidDanToc.setText(null);
            lbl_ValidDiaChi.setText(null);
            lbl_ValidEmail.setText(null);
            lbl_ValidHoTen.setText(null);
            lbl_ValidMaPH.setText(null);
            lbl_ValidNamSinh.setText(null);
            lbl_ValidSDT.setText(null);
            
        } else if(btc.luaChonPH==2){
            btn_PHCu.setSelected(true);
            
            txt_MaPHCu.setText(btc.maND);
        }
         
    }
