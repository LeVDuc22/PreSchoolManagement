    private void btn_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_OKActionPerformed
        if(btn_PHMoi.isSelected()){
            
            NguoiDung ph = new NguoiDung();
        try{
            ph.setMaND(txt_MaPH.getText());
            ph.setHoTen(txt_HoTen.getText());
            ph.setDanToc(txt_DanToc.getText());
            ph.setTenDN(txt_MaPH.getText());
            ph.setCccd(txt_CCCD.getText());
            ph.setDiaChi(txt_DiaChi.getText());
            ph.setSoDT(txt_SDT.getText());
            ph.setEmail(txt_Email.getText());
            ph.setMatKhau(txt_MaPH.getText());

             //set giới tính
            if(btn_Nam.isSelected()){
               ph.setGioiTinh(true);
            } else if(btn_Nu.isSelected()){
                ph.setGioiTinh(false);
            }

            ph.setNamSinh(Integer.parseInt(txt_NamSinh.getText()));
            ph.setVaiTro("phuhuynh");
            
            BienToanCuc btc = new BienToanCuc();
            btc.phuhuynh = ph;
            btc.luaChonPH =1;
            
            this.dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Dữ liệu trống hoặc không đúng định dạng, vui lòng kiểm tra lại!");
            }
        } else if(btn_PHCu.isSelected()){
            
            BienToanCuc btc = new BienToanCuc();
            btc.maND = txt_MaPHCu.getText();
            btc.luaChonPH=2;
            this.dispose();
        }
        
        
        
    }//GEN-LAST:event_btn_OKActionPerformed

