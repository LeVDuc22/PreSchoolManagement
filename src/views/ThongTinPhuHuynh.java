    private void txt_NamSinhPHFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_NamSinhPHFocusLost
        if(this.isVisible()){
            String str = txt_NamSinhPH.getText();
            String dinhDang = "^(([12][0-9][0-9][0-9]))$";

            if(str.length()>0){
                if(! str.matches(dinhDang)){
                    JOptionPane.showMessageDialog(rootPane, "Sai định năm sinh!");
                    txt_NamSinhPH.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ngày sinh không được để trống!");
                txt_NamSinhPH.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_NamSinhPHFocusLost

