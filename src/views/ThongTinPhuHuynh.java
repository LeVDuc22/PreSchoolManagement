    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if(btnPHMoi.isSelected()){
            
            NguoiDung ph = new NguoiDung();
        try{
            ph.setMaND(txt_MaPH.getText());
            ph.setHoTen(txt_HoTenPH.getText());
            ph.setDanToc(txt_DanTocPH.getText());
            ph.setTenDN(txt_MaPH.getText());
            ph.setCccd(txt_CCCD.getText());
            ph.setDiaChi(txt_DiaChi.getText());
            ph.setSoDT(txt_SDT.getText());
            ph.setEmail(txt_Email.getText());
            ph.setMatKhau(txt_MaPH.getText());

             //set giới tính
            if(btnNam.isSelected()){
               ph.setGioiTinh(true);
            } else if(btnNu.isSelected()){
                ph.setGioiTinh(false);
            }

            ph.setNamSinh(Integer.parseInt(txt_NamSinhPH.getText()));
            ph.setVaiTro("phuhuynh");
            
            BienToanCuc_QLHS btc = new BienToanCuc_QLHS();
            btc.setPhuhuynh(ph);
            btc.setLuaChonPH(1);
            
            this.dispose();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Dữ liệu trống hoặc không đúng định dạng, vui lòng kiểm tra lại!");
            }
        } else if(btnPHCu.isSelected()){
            
            BienToanCuc_QLHS btc = new BienToanCuc_QLHS();
            btc.setMaND(txtMaPHCu.getText());
            btc.setLuaChonPH(2);
            this.dispose();
        }
        
        
        
    }//GEN-LAST:event_btnOKActionPerformed

