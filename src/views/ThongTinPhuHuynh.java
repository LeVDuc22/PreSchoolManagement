    //Hàm load lại Thông tin PH lên biểu mẫu
    public void HienThiThongTinPH(BienToanCuc_QLHS btc){
        NguoiDung ph = btc.getPhuhuynh();
        
        if(btc.luaChonPH == 1){
            btnPHMoi.setSelected(true);
            txt_MaPH.setText(ph.getMaND());
            txt_TenDN.setText(ph.getTenDN());
            txt_HoTenPH.setText(ph.getHoTen());
            txt_NamSinhPH.setText(ph.getNamSinh()+"");

            if(ph.isGioiTinh()){
                btnNam.setSelected(true);
            } else {
                btnNu.setSelected(true);
            }

            txt_DanTocPH.setText(ph.getDanToc());
            txt_CCCD.setText(ph.getCccd());
            txt_DiaChi.setText(ph.getDiaChi());
            txt_SDT.setText(ph.getSoDT());
            txt_Email.setText(ph.getEmail());
            
        } else if(btc.luaChonPH==2){
            btnPHCu.setSelected(true);
            
            txtMaPHCu.setText(btc.getMaND());
        }
        
    }
    
    
